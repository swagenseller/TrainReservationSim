CREATE TABLE Trains(
  ID INT PRIMARY KEY,
  TRAIN_NUM INT
);

CREATE TABLE TrainSchedule(
  ID INT PRIMARY KEY,
  TRAIN_ID INT,
  SCHEDULE_ID TEXT
);

CREATE TABLE StationSchedule(
  ID INT PRIMARY KEY,
  STATION_ID_A TEXT,
  STATION_ID_D TEXT,
  ARRIVAL_TIME INT,
  DEPARTURE_TIME INT
);

CREATE TABLE Stations (
  ID TEXT PRIMARY KEY,
  STATION_NAME TEXT
);
